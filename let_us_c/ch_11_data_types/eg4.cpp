#include <iostream>
using namespace std;

int main(){
    char c;
    unsigned char d;
    int i;
    unsigned int j;
    short int k;
    unsigned short int l;
    long int m;
    unsigned long int n;
    float x;
    double y;
    long double z;

    /* char */
    cin>>c>>d;
    cout<<endl<<c<<" "<<d<<endl;

    /* int */
    cin>>i>>j;
    cout<<endl<<i<<" "<<j<<endl;

    /* short int */
    cin>>k>>l;
    cout<<endl<<k<<" "<<l<<endl;

    /* long int */
    cin>>m>>n;
    cout<<endl<<m<<" "<<n<<endl;

    /* float,double,long double */
    cin>>x>>y>>z;
    cout<<endl<<x<<" "<<y<<" "<<z<<endl;
    
    return 0;
}